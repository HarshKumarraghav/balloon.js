const getWord = () => {
  const raw = words[Math.floor(Math.random() * Math.floor(words.length))];
  const stylish = raw.charAt(0).toUpperCase() + raw.slice(1);
  return stylish;
};

export const getRandomProjectNameHandler = () => {
  const randName = `${getWord()}-${getWord()}`?.toLowerCase();
  return randName;
};
